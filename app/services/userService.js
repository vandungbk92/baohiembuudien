import axios from 'axios';
import { API } from '@api';

function getAll(page, limit, query) {
  return axios.get(`${API.USER_QUERY.format(page, limit, query)}`).then(res => {
    if (res.data) {
      return res.data;
    }
    else {
      return null
    }
  })
    .catch(error => {
      return null
    });
}

